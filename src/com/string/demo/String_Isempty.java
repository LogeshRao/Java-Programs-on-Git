package com.string.demo;

public class String_Isempty {
public static void main(String[] args) {
	String s1 = "null";
	String s2 = "";
	String s3 = " ";
	String s4 = null;
	System.out.println("s1 is empty or not : "+s1.isEmpty());
	System.out.println("s2 is empty or not : "+s2.isEmpty());
	System.out.println("s3 is empty or not : "+s3.isEmpty());
	System.out.println("s4 is empty or not : "+s4.isEmpty());	
}
}
